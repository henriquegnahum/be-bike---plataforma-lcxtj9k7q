const EN: typeof PT = { ...PT, dashboard: 'Be Bike OS 2.0', hubs: 'Hubs Dashboard' }
const ES: typeof PT = { ...PT, dashboard: 'Be Bike OS 2.0', hubs: 'Panel Hubs' }

const dict = { PT, EN, ES }

export function useTranslation() {
  const { language } = useAppStore()

  return useCallback(
    (key: keyof typeof PT, params?: Record<string, string>) => {
      const dictObj = dict[language] || dict.PT
      let str = dictObj[key] || key
      if (params) {
        Object.entries(params).forEach(([k, v]) => {
          str = str.replace(`{${k}}`, v)
        })
      }
      return str
    },
    [language],
  )
}
